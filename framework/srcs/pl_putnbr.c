#include "../inc/libunit.h"

void	pl_putnbr(int n)
{
	if (n == -2147483648)
	{
		pl_putnbr(n / 10);
		n = 8;
	}
	if (n < 0)
	{
		pl_putchar('-');
		n *= -1;
	}
	if (n > 9)
		pl_putnbr(n / 10);
	pl_putchar((n % 10) + 48);
}
