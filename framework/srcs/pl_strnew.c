#include "../inc/libunit.h"

char	*pl_strnew(size_t size)
{
	char *s;

	s = (char*)malloc(sizeof(*s) * (size + 1));
	if (!s)
		return (NULL);
	else
		pl_bzero(s, size + 1);
	return (s);
}
