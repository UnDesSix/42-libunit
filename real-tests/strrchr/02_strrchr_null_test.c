#include "../real_tests.h"

int		strrchr_null_test(void)
{
	char	*s1;
	char	*s2;

	s1 = NULL;
	s2 = ft_strrchr(s1, 'z');
	if (pl_strcmp(s2, "z") == 0)
		return (0);
	else
		return (-1);
}
